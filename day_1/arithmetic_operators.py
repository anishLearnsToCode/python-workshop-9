a = 10
b = 3 # b <--- 3 assignment operator

# addition
print(a + b)

# subtraction
print(a - b)

# multiplication
print(a * b)

# division --> float
print(a / b)

# floor division floor(a / b) --> int
# a // b = floor(a / b)
print(a // b)

# power operator (a^b)
print(a ** b)

# Modulus Operator (a / b --> remainder ??)
print(a % b) # a mod b
